weather = 'sunny'

case weather
when 'sunny'
    puts "Time to whip out your favourite summer dress girl!"
when 'windy'
    puts "Wear something that won't end in you flashing the world today ;)!"
when 'rainy'
    puts "Get ready, it's going to be a wet day! Don't forget to take your rain coat and umbrella."
when 'cloudy'
    puts "You can dress however you like, but remember your jumper!"
when 'snowing'
    puts "Put as many layers on as you possibly can, it's gonna be a cold one!"
else
    puts "Heck if I know, wear what you think is best!"
end